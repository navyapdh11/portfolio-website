// Compact suburb generator: [name, postcode]
const S = (n: string, p: string, st: string): Suburb => ({
	name: n,
	slug: n
		.toLowerCase()
		.replace(/[^a-z0-9]+/g, "-")
		.replace(/(^-|-$)/g, ""),
	postcode: p,
	state: st,
});

